package base;

public class Conversion {
	public static final int Hours = 1;

	public static final int HoursPerDay = 24;
	public static final int Days = HoursPerDay;

	public static final int DaysPerWeek = 7;
	public static final int HoursPerWeek = HoursPerDay * DaysPerWeek;
	public static final int Weeks = HoursPerWeek;

	public static final int DaysPerMonth = 30;
	public static final int HoursPerMonth = HoursPerDay * DaysPerMonth;
	public static final int Months = HoursPerMonth;

	public static final int DaysPerYear = 365;
	public static final int HoursPerYear = HoursPerDay * DaysPerYear;
	public static final int Years = HoursPerYear;
}

